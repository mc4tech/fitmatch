//A Get request to scrape website
app.get("/scrape", function(req, res) {
  // First, we grab the body of the html with request
  request("https://www.runnersworld.com/", function(error, response, html) {
    // Then, we load that into cheerio and save it to $ for a shorthand selector
    var $ = cheerio.load(html);
    // Now, we grab every div with an article-tile class and do the following:
    $(".article-title").each(function(i, element) {

      // Save an empty result object
      var result = {};

      // Add the text and href of every link, and save them as properties of the result object
      result.title = $(this).children("a").text();
      result.link = $(this).children("a").attr("href");

      // Using our ScraperArticle model, create a new entry
      // This effectively passes the result object to the entry (and the title and link)
      var entry = new ScraperArticle(result);

      // Now, save that entry to the db
      entry.save(function(err, doc) {
        // Log any errors
        if (err) {
          console.log(err);
        }
        // Or log the doc
        else {
          console.log(doc);
        }
      });

    });
  });
  // Tell the browser that we finished scraping the text
  res.send("Scrape Complete");
});

